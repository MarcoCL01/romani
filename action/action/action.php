    XXXI. PRECETTI DELLA GUERRA NAVALE.

    Per ordine della tua Maestà, o invitto imperatore [12], avendo portato a termine il trattato relativo alla guerra terrestre, manca ancora, a mio avviso, quella parte che riguarda la guerra navale ["navalis belli"], sulla cui dottrina c’è pochissimo da dire, giacché, 
    essendo da un pezzo pacificato il mare ["pacato mari"], non si tratta che di condurre contro i barbari delle battaglie terrestri ["terrestre certamen"]. [13],
    Il popolo romano, per il suo prestigio e per le esigenze della sua grandezza, pur non essendovi costretto da alcun imminente pericolo, in ogni tempo mantenne allestita la flotta ["classem"], onde averla sempre pronta ["praeparatam"] ad ogni necessità. 
    Indubbiamente, nessuno osa sfidare o arrecare danno a quel regno o popolo, che sa essere pronto a combattere e risoluto a resistere ed a vendicarsi.
    Pertanto, con le flotte ["cum classibus"] erano stanziate una legione presso Miseno ed una presso Ravenna, sia perché non si allontanassero eccessivamente dalla difesa di Roma, 
    sia perché, all’insorgere di un’esigenza, potessero recarsi con le navi ["navigio"], senza indugio e senza dover aggirare la Penisola, in qualsiasi parte del mondo.
    Infatti la flotta Misenense ["Misenatium classis"] aveva nelle sue vicinanze la Gallia, le Spagne, la Mauretania, l'Africa, l'Egitto, la Sardegna e la Sicilia. 
    La flotta Ravennate ["classis Ravennatium"] soleva raggiungere con navigazione diretta ["directa navigatione"] l'Epiro, la Macedonia, la Grecia, la Propontide, il Ponto, l'Oriente, Creta e Cipro. Ciò perché nelle operazioni belliche la celerità giova di solito più del valore.
    <br><br>
    XXXII. DENOMINAZIONE DEI COMANDANTI IN UNA FLOTTA.

    Il comandante della flotta ["praefectus classis"] Misenense era preposto alle navi da guerra ["liburnis"] che erano di base in Campania, mentre il comandante della flotta Ravennate reggeva quelle che erano nelle acque del mare Ionio [incluso l’Adriatico] [14],
    Alle dipendenze di ciascuno di loro vi erano dieci tribuni ["deni tribuni"] delegati per le singole coorti.
    Ogni galea aveva il proprio comandante ["navarchus"], che è all’incirca l’equivalente dell’armatore ["navicularius", per le navi mercantili], il quale, oltre agli altri compiti nautici ["nautarum officiis"], curava l’addestramento quotidiano dei timonieri ["gubernatoribus"], dei rematori ["remigibus"] e dei militi navali ["militibus"].
